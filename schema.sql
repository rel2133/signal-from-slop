CREATE INDEX IF NOT EXISTS idx_mentions_run_ticker ON item_ticker_mentions(analysis_run_id, ticker);
CREATE INDEX IF NOT EXISTS idx_buckets_run_ticker ON ticker_time_buckets(analysis_run_id, ticker);
