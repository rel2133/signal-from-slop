CREATE TABLE IF NOT EXISTS sources (
    source_id INTEGER PRIMARY KEY AUTOINCREMENT,
    source_key TEXT NOT NULL UNIQUE,
    source_type TEXT NOT NULL,
    display_name TEXT NOT NULL,
    normalized_value TEXT NOT NULL,
    url TEXT NOT NULL,
    active INTEGER NOT NULL DEFAULT 1,
    date_added TEXT NOT NULL,
    notes TEXT NOT NULL DEFAULT ''
);

CREATE TABLE IF NOT EXISTS analysis_runs (
    analysis_run_id TEXT PRIMARY KEY,
    started_at TEXT NOT NULL,
    completed_at TEXT,
    status TEXT NOT NULL,
    data_mode TEXT NOT NULL,
    selected_source_ids TEXT NOT NULL,
    time_window_label TEXT NOT NULL,
    time_window_start TEXT NOT NULL,
    time_window_end TEXT NOT NULL,
    max_posts_per_source INTEGER NOT NULL,
    max_comments_per_thread INTEGER NOT NULL,
    include_comments INTEGER NOT NULL,
    run_deeper_analysis INTEGER NOT NULL,
    ollama_model TEXT NOT NULL,
    ollama_url TEXT NOT NULL,
    summary_json TEXT NOT NULL DEFAULT '{}'
);

CREATE TABLE IF NOT EXISTS reddit_items (
    item_id TEXT PRIMARY KEY,
    source_id INTEGER,
    source_type TEXT NOT NULL,
    source_name TEXT NOT NULL,
    subreddit TEXT,
    thread_id TEXT NOT NULL,
    thread_title TEXT NOT NULL,
    thread_url TEXT NOT NULL,
    comment_id TEXT,
    parent_id TEXT,
    item_type TEXT NOT NULL,
    body_text TEXT NOT NULL,
    author TEXT NOT NULL,
    author_hash TEXT NOT NULL,
    score INTEGER NOT NULL,
    created_time TEXT NOT NULL,
    permalink TEXT NOT NULL,
    comment_depth INTEGER NOT NULL,
    raw_json TEXT NOT NULL,
    FOREIGN KEY (source_id) REFERENCES sources(source_id)
);

CREATE TABLE IF NOT EXISTS classifications (
    analysis_run_id TEXT NOT NULL,
    item_id TEXT NOT NULL,
    sentiment TEXT NOT NULL,
    confidence REAL NOT NULL,
    tickers TEXT NOT NULL,
    company_names TEXT NOT NULL,
    summary TEXT NOT NULL,
    bull_case TEXT NOT NULL,
    bear_case TEXT NOT NULL,
    evidence_quality TEXT NOT NULL,
    depth_score INTEGER NOT NULL,
    hype_score INTEGER NOT NULL,
    claim_specificity_score INTEGER NOT NULL,
    repetition_score INTEGER NOT NULL,
    needs_deeper_analysis INTEGER NOT NULL,
    red_flags TEXT NOT NULL,
    claims_to_verify TEXT NOT NULL,
    alpha_signal_score REAL NOT NULL,
    hype_adjusted_signal_score REAL NOT NULL,
    model_name TEXT NOT NULL,
    classifier_mode TEXT NOT NULL,
    deeper_analysis_json TEXT NOT NULL DEFAULT '{}',
    analyzed_at TEXT NOT NULL,
    PRIMARY KEY (analysis_run_id, item_id),
    FOREIGN KEY (analysis_run_id) REFERENCES analysis_runs(analysis_run_id),
    FOREIGN KEY (item_id) REFERENCES reddit_items(item_id)
);

